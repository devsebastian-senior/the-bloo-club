export const BREEDS: Breed[] = ROWS.map(([name, size, kg]) => ({
  id: name
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "") // strip accents
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, ""),
  name,
  size,
  typicalKg: kg,
}));

export const MIXED_BREED_ID = "mixed";

export function getBreed(id: string | null): Breed | null {
  if (!id) return null;
  return BREEDS.find((b) => b.id === id) ?? null;
}
